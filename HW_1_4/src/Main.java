public class Main {
    public static void main(String[] args) {
        //task first
        int i = 1;
        while (i <= 10)
            System.out.print(i++ + " ");
            System.out.println("\n");
        for (int j = 10; j != 0; j--)
            System.out.print(j + " ");
            System.out.println("\n");
    }
}